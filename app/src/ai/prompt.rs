/// 会話セッションを長期記憶へ落とすための要約用プロンプト。
///
/// MATOME_PROMPT(/imakita 用)とは目的が違うので別に持つ。あちらは読み捨てのユーザー向け出力、
/// こちらは後日 recall で引かれる記憶であり、発言者と伝聞性を保存する必要がある。
/// ログ本文にはチャンネル名が含まれないため、チャンネル名はプロンプト末尾(## このログのチャンネル)に呼び出し側が添える。
pub const MEMORY_SUMMARY_PROMPT: &str = r"
## 指示
与えられたチャットログは、あるチャンネルでひと段落した会話です。
これを「まなみが後日思い出すための記憶」として要約しなさい。

## NOTE: 事実として断定しないこと
チャンネルによっては、そこで語られた内容は現実の出来事ではありません（夢日記、創作、ネタなど）。
末尾に示されるチャンネル名を必ず踏まえなさい。
「宇田は空を飛んだ」ではなく「宇田が空を飛んだ夢の話をしていた」のように、
必ず「誰々が〜と言っていた」「〜という話をしていた」という伝聞の形で書きなさい。
誰の発言かを必ず残しなさい。

## 残すべき情報
- 誰が何を言ったか
- そのときのそのチャンネルでの話題、トピック、およびその詳細
- 決まったこと、約束したこと、予定
- 各人の好み・事実・状況の変化
- 話題になった固有名詞（作品名・製品名・人名・地名・技術名など）は原文のまま残す
- 未解決の質問や、持ち越しになった話

## 落としてよい情報
- 挨拶・相槌などの短い反応
- コマンドの実行結果（ダイスの出目、計算結果、エラー出力などの機械的な出力）

## 出力形式
- 箇条書きの日本語。全体で400文字以内の要約。
- 前置き・後書き・見出しは書かない。要約本文のみを出力する。
- 記憶として残す価値のある内容が一つも無い場合は、`SKIP` の4文字だけを出力する。
";

